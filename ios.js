// Function to pass events from one canvas to another
function passEvent(event, targetCanvas) {
    // Create a new event with the same type and properties
    const newEvent = new event.constructor(event.type, event);

    // Adjust the clientX, clientY for the target canvas (if necessary)
    const boundingRect = targetCanvas.getBoundingClientRect();
    const offsetX = event.clientX - originalCanvas.getBoundingClientRect().left;
    const offsetY = event.clientY - originalCanvas.getBoundingClientRect().top;

    // Set the new coordinates relative to the second canvas
    Object.defineProperty(newEvent, 'clientX', { value: boundingRect.left + offsetX });
    Object.defineProperty(newEvent, 'clientY', { value: boundingRect.top + offsetY });

    // Dispatch the new event on the second canvas
    targetCanvas.dispatchEvent(newEvent);
}

